    private int getIdCliente() {
        int id = 0;

        try {

            ResultSet rs;

            Util util = new Util();
            Connection conexion = util.CreaConexion();
            String consulta = "SELECT id FROM SSB_Cliente WHERE nombre=(?)";
            PreparedStatement ps = conexion.prepareStatement(consulta);

            ps.setString(1, combo_clientes.getSelectedItem().toString());

            rs = ps.executeQuery();

            //ASIGNAMOS EN ID EL ID DEL CLIENTE SELECCIONADO PREVIAMENTE DESDE LA CONSULTA
            while (rs.next()) {
                id = rs.getInt("id");
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "ERROR " + e.toString());

        }

        return id;
    }
