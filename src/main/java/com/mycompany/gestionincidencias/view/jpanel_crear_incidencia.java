    private void btn_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearActionPerformed
        int id_cliente = 0;

        //COMPROBAMOS SI EL COMBOBOX DE ESTADO Y PRIORIDAD ESTÁN SELECCIONADOS
        if (combo_estado.getSelectedItem().equals(null)) {
            JOptionPane.showMessageDialog(null, "Error, el campo estado está vacío", "ERROR", 0);
        } else if (combo_prioridad.getSelectedItem().equals(null)) {
            JOptionPane.showMessageDialog(null, "Error, el campo prioridad está vacío", "ERROR", 0);
        } else {

            try {
                // TODO add your handling code here:
                id_cliente = getCliente(combo_cliente.getSelectedItem().toString());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(jpanel_crear_incidencia.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(jpanel_crear_incidencia.class.getName()).log(Level.SEVERE, null, ex);
            }

            //CREAMOS UN OBJETO TICKET QUE PASAREMOS POSTERIORMENTE AL MÉTODO INSERTAR
            Ticket ticket = new Ticket(txt_nombre.getText(), txt_descripcion.getText(), combo_estado.getSelectedItem().toString(), combo_prioridad.getSelectedItem().toString(), id_cliente);

            //INSTANCIAMOS TICKETDAO PARA REALIZAR ACCIONES
            TicketDAO ticketsdao = new TicketDAO();

            try {

                //HACEMOS USO DEL MÉTODO INSERTARTICKET PARA INSERTAR EL TICKET PASADO POR PARÁMETRO
                ticketsdao.insertarTicket(ticket, usuario);
                txt_nombre.setText("");
                txt_descripcion.setText("");

            } catch (ParseException ex) {
                Logger.getLogger(jpanel_crear_incidencia.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(jpanel_crear_incidencia.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_btn_crearActionPerformed

