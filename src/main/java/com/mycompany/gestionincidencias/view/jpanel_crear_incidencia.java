    public void rellenarClientes() throws ClassNotFoundException, SQLException {

        ResultSet rs;

        Util util = new Util();
        Connection conexion = util.CreaConexion();
        String consulta = "SELECT nombre FROM SSB_Cliente";
        PreparedStatement ps = conexion.prepareStatement(consulta);

        rs = ps.executeQuery();

        while (rs.next()) {
            //MIENTRAS HAYA DATOS AÑADIMOS ITEMS AL COMBOBOX
            combo_cliente.addItem(rs.getString("nombre"));
        }

    }

    public int getCliente(String cliente) throws ClassNotFoundException, SQLException {

        int id = 0;

        ResultSet rs;

        Util util = new Util();
        Connection conexion = util.CreaConexion();
        String consulta = "SELECT id FROM SSB_Cliente WHERE nombre=(?)";
        PreparedStatement ps = conexion.prepareStatement(consulta);

        ps.setString(1, cliente);
        rs = ps.executeQuery();

        while (rs.next()) {
            //MIENTAS HAYA DATOS, ASIGNAMOS EN ID EL ID DEL CLIENTE
            id = rs.getInt("id");
        }

        return id;
    }

